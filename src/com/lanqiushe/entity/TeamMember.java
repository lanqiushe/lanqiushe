package com.lanqiushe.entity;
/**
 * 球员信息
 * @author lee
 *
 */
public class TeamMember {
    /**
    * 队员头像   
    */
	public String teamMemberHead;
	/**
	 * 该队员的名字
	 */
	public String teamMemberName;
	/**
	 * 队员在球队中的位置
	 */
	public String teamMemberLocation;
	
	
	
	/**
	 * 该队员所属队名
	 */
	public String teamName;
	
	
	
	/**
	 * 队员数量
	 */
	public String teamMemberNumber;
	/**
	 * 距离
	 */
	public String distance;
}
